package org.xlrnet.datac.commons.ui;

/**
 * Constants for use with the Datac theme.
 */
public class DatacTheme {

    /**
     * Wide textfield.
     */
    public static final String FIELD_WIDE = "field-wide";

    /**
     * Render as list.
     */
    public static final String LIST_LAYOUT = "list-layout";

    /**
     * Adds a small padding to the right.
     */
    public static final String CAPTION_PADDING_RIGHT = "caption-padding-right";

    /**
     * Styles the text input like a password box. This is used to avoid autocompletion by browsers and should never be
     * used for actual user passwords!
     */
    public static final String INPUT_PASSWORD = "input-password";       // NOSONAR: This is just a CSS class name
}
